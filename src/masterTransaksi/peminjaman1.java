    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
      String id = cb_idGuru.getSelectedItem().toString();
    String namaGuru = tx_namaGuru.getText();
    String jenisPinjaman = cb_jenisPinjaman.getSelectedItem().toString();

    int jumlahPinjaman = 0;

    try {
        // Cek input tanggal kosong
        if (jDate_tglPinjaman.getDate() == null || jDate_jatuhTempo.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Tanggal pinjam dan jatuh tempo wajib diisi!");
            return;
        }

        // Cek input jumlah
       if (!tx_totalPinjaman.getText().isEmpty()) {
    // Total pinjaman diinput manual, pastikan format angka valid (hanya titik jika ada desimal)
    double total = Double.parseDouble(tx_totalPinjaman.getText());
    jumlahPinjaman = (int) Math.round(total); // bulatkan ke int
} else {
    // Hitung dari jumlah pinjaman + bunga
    double jumlah = Double.parseDouble(tx_jumlahPinjaman.getText());
    double persen = Double.parseDouble(tx_persenBunga.getText());

    double bunga = (jumlah * persen) / 100;
    double total = jumlah + bunga;

    jumlahPinjaman = (int) Math.round(total); // bulatkan ke int
}


        java.sql.Date tglPinjam = new java.sql.Date(jDate_tglPinjaman.getDate().getTime());
        java.sql.Date tglJatuhTempo = new java.sql.Date(jDate_jatuhTempo.getDate().getTime());

        if (btn_simpan.getText().equalsIgnoreCase("SIMPAN")) {
            sql = "INSERT INTO pinjaman_guru (Id, nama_guru, jenis_pinjaman, "
                + "jumlah_pinjaman, tanggal_pinjam, tanggal_jatuh_tempo) "
                + "VALUES (?, ?, ?, ?, ?, ?)";

            PreparedStatement pst = cn.prepareStatement(sql);
            pst.setString(1, id);
            pst.setString(2, namaGuru);
            pst.setString(3, jenisPinjaman);
            pst.setInt(4, jumlahPinjaman);
            pst.setDate(5, tglPinjam);
            pst.setDate(6, tglJatuhTempo);

            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan!");
        }

        bersih();
        TampilData();

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Input angka tidak valid (gunakan titik untuk desimal): " + e.getMessage());
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal menyimpan data: " + e.getMessage());
    }
    }//GEN-LAST:event_btn_simpanActionPerformed

