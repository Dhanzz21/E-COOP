   private void hitungTotalPinjamanOtomatis() {
    try {
        double jumlah = Double.parseDouble(tx_jumlahPinjaman.getText().replace(",", "."));
        double persen = Double.parseDouble(tx_persenBunga.getText().replace(",", "."));

        double bunga = (jumlah * persen) / 100.0;
        double total = jumlah + bunga;

        // Format hasil ke string dengan 2 angka di belakang koma, menggunakan titik sebagai desimal
        tx_totalBunga.setText(String.format(Locale.US, "%.2f", bunga));
        tx_totalPinjaman.setText(String.format(Locale.US, "%.2f", total));

    } catch (NumberFormatException e) {
        tx_totalBunga.setText("");
        tx_totalPinjaman.setText("");
    }
}



    
    public void TampilData() {
        try {
            DefaultTableModel model = new DefaultTableModel(
                new String[]{"Id", "Nama Guru", "Jenis Pinjaman",
                             "Jumlah Pinjaman", "Tanggal Pinjam", "Jatuh Tempo"}, 0);

            stat = cn.createStatement();
            ResultSet rs = stat.executeQuery("SELECT * FROM pinjaman_guru");
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("Id"),
                    rs.getString("nama_guru"),
                    rs.getString("jenis_pinjaman"),
                    rs.getInt("jumlah_pinjaman"),
                    rs.getDate("tanggal_pinjam"),
                    rs.getDate("tanggal_jatuh_tempo")
                });
            }
            tbldatapinjaman1.setModel(model);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this,
                "Gagal menampilkan data: " + e.getMessage());
        }
    }    

    private void kodeBarang() {
        if (cb_idGuru.getSelectedItem() == "Pilih Id Guru") {
            return;
        }

        String kode = cb_idGuru.getSelectedItem().toString();

        try {
            sql = "SELECT nama_guru FROM data_guru WHERE Id = ?";
            PreparedStatement pst = cn.prepareStatement(sql);
            pst.setString(1, kode);
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                tx_namaGuru.setText(rs.getString("nama_guru"));

                // Set fokus dan tidak bisa diedit
            } else {
                // Jika data tidak ditemukan
                tx_namaGuru.setText("");
                JOptionPane.showMessageDialog(this, "Data guru tidak ditemukan.");
            }

            // Tutup statement dan result
            rs.close();
            pst.close();

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal mengambil data guru: " + e.getMessage());
        }

    }

