    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
         try {
    stat = cn.createStatement();

    // Validasi input kosong
    if (tx_Id.getText().equals("") ||
        tx_namaGuru.getText().equals("") ||
        tx_nip.getText().equals("")) {
        JOptionPane.showMessageDialog(null, "Data tidak boleh kosong!", "Validasi Data", JOptionPane.INFORMATION_MESSAGE);
        return;
    }

    if (btn_simpan.getText().equals("SIMPAN")) {
        // Cek apakah ID sudah ada
        String cek = "SELECT * FROM data_guru WHERE id='" + tx_Id.getText() + "'";
        res = stat.executeQuery(cek);
        if (res.next()) {
            JOptionPane.showMessageDialog(null, "ID sudah ada!");
        } 
        else {
            // Insert data baru
            String sql = "INSERT INTO data_guru VALUES ('" + tx_Id.getText() +
             "','" + tx_namaGuru.getText() +
             "','" + cb_status.getSelectedItem().toString() +
             "','" + tx_nip.getText() + "')";

            stat.executeUpdate(sql);
            JOptionPane.showMessageDialog(null, "Berhasil input data!");
            bersih();
            main_panel.removeAll();
            main_panel.repaint();
            main_panel.revalidate();
        
            main_panel.add(data_guru);
            main_panel.repaint();
            main_panel.revalidate();
            TampilData();
        } 
        

    } else{
            String edit = "UPDATE data_guru SET Nama_Guru = '" + tx_namaGuru.getText() +
              "', Status = '" + cb_status.getSelectedItem().toString() +
              "', Nip = '" + tx_nip.getText() +
              "' WHERE Id = '" + tx_Id.getText() + "'";

        stat.executeUpdate(edit);
        JOptionPane.showMessageDialog(null, "Data berhasil diubah!");
              main_panel.removeAll();
              main_panel.repaint();
              main_panel.revalidate();
        
              main_panel.add(data_guru);
              main_panel.repaint();
              main_panel.revalidate(); 
        TampilData();
                
        }
        bersih();
} catch (Exception e) {
    JOptionPane.showMessageDialog(null, "Terjadi kesalahan: " + e.getMessage());
}
    }//GEN-LAST:event_btn_simpanActionPerformed

