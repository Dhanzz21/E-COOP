    private void CariData() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("Id");
    model.addColumn("Nama Guru");
    model.addColumn("Status");
    model.addColumn("Nip");

    String cari = tx_cari.getText(); // txtCari adalah JTextField buat input pencarian

    try {
        String sql = "SELECT * FROM data_guru WHERE " +
                     "Id LIKE ? OR " +
                     "Nama_Guru LIKE ? OR " +
                     "Status LIKE ? OR " +
                     "Nip LIKE ?";
        
        java.sql.Connection conn = (java.sql.Connection) koneksi.BukaKoneksi();
        java.sql.PreparedStatement pst = conn.prepareStatement(sql);

        for (int i = 1; i <= 4; i++) {
            pst.setString(i, "%" + cari + "%");
        }

        java.sql.ResultSet rs = pst.executeQuery();

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("Id"),
                rs.getString("Nama_Guru"),
                rs.getString("Status"),
                rs.getString("Nip")
            });
        }

        tbldataGuru.setModel(model); // tabelBarang adalah JTable buat nampilin data

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Gagal menampilkan data: " + e.getMessage());
    }
}

     
public void TampilData() {
    try {
        String sql = "SELECT * FROM data_guru ORDER BY Id ASC"; 
        stat = cn.createStatement();
        res = stat.executeQuery(sql); // gunakan query yg udah disiapin

        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Nama Guru");
        model.addColumn("Status");
        model.addColumn("NIP/NIPPPK/NIPKTT");

        model.setRowCount(0); // cukup ini aja, udah bersihin tabel

        while (res.next()) {
            Object[] data = {
                res.getString("Id"),
                res.getString("Nama_Guru"),
                res.getString("Status"),
                res.getString("Nip")
            };
            model.addRow(data);
        }

        tbldataGuru.setModel(model);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Gagal menampilkan data: " + e.getMessage());
    }
}

     
     

